# Addition
print(2 + 3)  # Output: 5

# Subtraction
print(5 - 2)  # Output: 3

# Multiplication
print(2 * 3)  # Output: 6

# Division
print(6 / 2)  # Output: 3.0

a = 6 // 2
print(a)  # Output: 3
print(type(a))  # Output: <class 'int'>

b = 6 / 2
print(b)  # Output: 3.0
print(type(b))  # Output: <class 'float'>

